package model;


public class Order
{
	private String dish;
	private String hungry;

	public String getDish()
	{
		return dish;
	}

	public void setDish(String dish)
	{
		this.dish = dish;
	}

	public String getHungry()
	{
		return hungry;
	}

	public void setHungry(String hungry)
	{
		this.hungry = hungry;
	}
}
